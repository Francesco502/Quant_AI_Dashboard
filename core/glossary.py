def get_tooltip_html(term: str, text: str = None) -> str:
    """
    生成带tooltip的HTML，鼠标悬停显示解释
    
    参数:
        term: 术语名称
        text: 显示的文本（如果为None，使用term）
    
    返回:
        HTML字符串
    """
    if text is None:
        text = term
    
    explanation = FINANCIAL_GLOSSARY.get(term, f"{term}的详细解释")
    
    return f"""
    <span class="term-tooltip" title="{explanation}">
        {text}
        <span class="tooltip-icon">ℹ</span>
    </span>
    """

def get_inline_explanation(term: str) -> str:
    """
    获取术语的简短解释（用于小字说明）
    
    参数:
        term: 术语名称
    
    返回:
        解释文本
    """
    return FINANCIAL_GLOSSARY.get(term, "")

